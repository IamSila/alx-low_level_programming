#include "3-calc.h"

/**
 * op_add - adds 2 integers together
 * @a: the first integers
 * @b: the second integers
 * Return: The sum which is an integer
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtract 2 integers together
 * @a: the first integers
 * @b: the second integers
 * Return: The difference which is an integer
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies 2 integers together
 * @a: the first integers
 * @b: the second integers
 * Return: The multiplication which is an integer
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides 2 integers together
 * @a: the first integers
 * @b: the second integers
 * Return: The quotient which is an integer
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - gets the reminder of  2 integers together
 * @a: the first integers
 * @b: the second integers
 * Return: The reminder which is an integer
 */

int op_mod(int a, int b)
{
	return (a % b);
}

